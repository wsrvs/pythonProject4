import random

# Список известных людей и их даты рождения
people = [
    ('Алексей Николаевич Толстой', '10.01.1883'),
    ('Владимир Высоцкий', '25.01.1938'),
    ('Александр Попов', '16.03.1859'),
    ('Николай Васильевич Гоголь', '01.04.1809'),
    ('Николай Склифосовский', '06.04.1836'),
    ('Сергей Прокофьев', '23.04.1891'),
    ('Владислав Третьяк', '25.04.1952'),
    ('Петр Ильич Чайковский', '07.05.1840'),
    ('Михаил Глинка', '01.06.1804'),
    ('Эрнесто Че Гевара', '14.06.1928')
]

# Функция для вывода даты рождения в формате "день месяц год"
def format_date(date_str):
    day, month, year = map(int, date_str.split('.'))
    months = ['января', 'февраля', 'марта', 'апреля', 'мая', 'июня',
              'июля', 'августа', 'сентября', 'октября', 'ноября', 'декабря']
    return f"{day} {months[month-1]} {year} года"

# Основная программа
while True:
    # Выбираем 5 случайных людей
    selected = random.sample(people, 5)

    # Просим пользователя ввести даты рождения
    correct_answers = 0
    for name, date in selected:
        user_input = input(f"Когда родился {name}? ")
        if user_input == date:
            print("Верно!")
            correct_answers += 1
        else:
            print(f"Неверно. Правильная дата рождения: {format_date(date)}")

    # Выводим результаты
    print(f"Вы ответили правильно на {correct_answers} из 5 вопросов.")
    print(f"Неправильных ответов: {5 - correct_answers}")

    # Предлагаем начать снова
    restart = input("Хотите сыграть еще раз? (y/n) ")
    if restart.lower() != 'y':
        break
